package com.paradisum.game.model;

/**
 * An enumeration that represents the different types of entities.
 * @author Carlos Aviles
 */
public enum EntityType {
	
	/**
	 * Represents a player controlled mob.
	 */
	PLAYER,
	
	/**
	 * Represents a non-player controlled mob.
	 */
	NPC,
	
	/**
	 * Represents a ground item.
	 */
	ITEM,
	
	/**
	 * Represents a game object.
	 */
	OBJECT;

}