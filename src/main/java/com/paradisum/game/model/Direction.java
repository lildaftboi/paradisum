package com.paradisum.game.model;

/**
 * An enumeration that represents the different movement directions.
 * @author Carlos Aviles
 */
public enum Direction {
	
	/**
	 * Represents a no direction.
	 */
	NONE,
	
	/**
	 * Represents the north point of a compass.
	 */
	NORTH,
	
	/**
	 * Represents the east point of a compass.
	 */
	EAST,
	
	/**
	 * Represents the south point of a compass.
	 */
	SOUTH,
	
	/**
	 * Represents the west point of a compass.
	 */
	WEST;

}